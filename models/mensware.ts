import mongoose from "mongoose";

const { Schema } = mongoose;

const menswareSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    imageUrl: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.models.Mensware || mongoose.model("Mensware", menswareSchema);